package br.com.uoutec.community.ediacaran.system.actions;

public class ScheduleActionAgainException extends Exception{

	private static final long serialVersionUID = 4467704322001043721L;

	public ScheduleActionAgainException() {
		super();
	}

	public ScheduleActionAgainException(String message, Throwable cause) {
		super(message, cause);
	}

	public ScheduleActionAgainException(String message) {
		super(message);
	}

	public ScheduleActionAgainException(Throwable cause) {
		super(cause);
	}

}
