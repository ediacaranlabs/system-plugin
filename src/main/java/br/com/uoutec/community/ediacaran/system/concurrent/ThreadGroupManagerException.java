package br.com.uoutec.community.ediacaran.system.concurrent;

public class ThreadGroupManagerException 
	extends Exception{

	private static final long serialVersionUID = 3864014223887880578L;

	public ThreadGroupManagerException() {
		super();
	}

	public ThreadGroupManagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public ThreadGroupManagerException(String message) {
		super(message);
	}

	public ThreadGroupManagerException(Throwable cause) {
		super(cause);
	}

}
